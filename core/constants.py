# 系统版本号
VERSION_MAJOR = '1'      # 大版本号
VERSION_MINOR = 284        # 小版本号（整数，每次修改+1）

def get_version_display():
    """
    获取格式化的版本号显示
    
    返回：
        格式化的版本号，如 "v1.001"
    """
    return f"v{VERSION_MAJOR}.{VERSION_MINOR:03d}"
